package com.resphere.android.modelo;

public class TipoPoblacion {
	private String hombres;
	private String mujeres;
	private String ninos;
	private String ninas;
	private String personas;
	private String hogares;
	public String getHombres() {
		return hombres;
	}
	public void setHombres(String hombres) {
		this.hombres = hombres;
	}
	public String getMujeres() {
		return mujeres;
	}
	public void setMujeres(String mujeres) {
		this.mujeres = mujeres;
	}
	public String getNinos() {
		return ninos;
	}
	public void setNinos(String ninos) {
		this.ninos = ninos;
	}
	public String getNinas() {
		return ninas;
	}
	public void setNinas(String ninas) {
		this.ninas = ninas;
	}
	public String getPersonas() {
		return personas;
	}
	public void setPersonas(String personas) {
		this.personas = personas;
	}
	public String getHogares() {
		return hogares;
	}
	public void setHogares(String hogares) {
		this.hogares = hogares;
	}
}
